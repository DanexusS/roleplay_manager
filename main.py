async def swap_items(guild, items, sender_id, other_id):
    for line in items.split("\n"):
        item, amount = line.split(" - ")
        for _ in range(int(amount[-1])):
            await remove_item(guild, sender_id, item)
            await add_item(guild, other_id, item)


